package com.company;

public class Main {

    public static void main(String[] args) {
	int x = 144, y = 999;

        System.out.println("Suma: " + x + " + " + y + " = " + (x + y));
        System.out.println("Resta: " + x + " - " + y + " = " + (x - y));
        System.out.println("División: " + x + " / " + y + " = " + ((double) x / (double) y));
        System.out.println("Multiplicación: " + x + " x " + y + " = " + (x * y));
    }
}
